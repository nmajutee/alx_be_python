# this script performs simple arithmetic operations 
number1 = 10
number2 = 5

# addition
addition = number1 + number2
print('Addition of {} and {} is {}.'.format(number1, number2, addition))

# subtraction
subtraction = number1 - number2
print('Subtraction of {} and {} is {}.'.format(number1, number2, subtraction))

# multiplication
multiplication = number1 * number2
print('Multiplication of {} and {} is {}.'.format(number1, number2, multiplication))
